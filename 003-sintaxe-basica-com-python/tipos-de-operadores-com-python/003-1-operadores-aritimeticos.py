# OPERADORES ARITIMÉTICOS


# Adição
print(1+1)
# >>> 2

# Subtração
print(10-2)
# >>> 8

# Multiplicação
print(4*3)
# >>> 12

# Divisao
print(12/3)
# >>> 4.0

# divisao inteira
print(10//2)
# >>> 5

# Módulo
print(10%3)
# >>> 1

# Exponenciação
print(2**3)
# >>> 8


# PRECEDÊNCIA DE OPERADORES
# Na matemática existe uma regra que indica quais operações devem ser executadas primeiro. Isso é útil pois ao analisar uma expressão, a depender da ordem das operações o valor pode ser diferente:
# x = 10 - 5 * 2
# x é igual a 10 ou 0


print(10 - 5 * 2)
# >>> 0

print ((10 - 5) * 2)
# >>> 10

print (10 ** 2 * 2)
# >>> 200

print (10 ** (2*2))
# >>> 10000

print(10/2*2)
# >>> 20.0