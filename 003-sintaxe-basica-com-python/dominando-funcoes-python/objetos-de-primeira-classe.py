# Objetos de primeira classe

# Em Python tudo é objeto, dessa forma funções também são objetos, 
# o que as tornam objetos de primeira classe. Com isso podemos atribuir funções a variáveis,
# passá-las como parâmetro para funções, usá-las como valores em estruturas de dados 
# (listas, tuplas, dicionários, etc) e usar como valor de retorno para uma função (closures).

