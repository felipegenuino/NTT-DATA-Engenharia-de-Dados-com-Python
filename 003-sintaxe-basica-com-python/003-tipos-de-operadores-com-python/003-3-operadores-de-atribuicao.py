# Conhecendo os operadores de atribuição

saldo = 500
print(saldo)
# >>> 500

saldo += 200
print(saldo)
# >>> 700

saldo -= 200
print(saldo)
 


saldo *= 200
print(saldo)


saldo //= 5
print(saldo)


saldo /= 5
print(saldo)




# === Atribuição com módulo
saldo2 = 500
saldo2 %= 480
print(saldo2)
# >>> 20

# === Atribuição com exponenciação
saldo3 = 80
saldo3 **= 2
print(saldo3)
# >>> 6400